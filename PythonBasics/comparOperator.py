#Comparision Operators

# Equal to ('=='):Checks if two values are equal.

x = 7
y = 5
print(x == y)  # False


#Not equal to ('!='):Checks if two values are not equal.

x = 5
y = 3
print(x != y) #Output: True

#Greater than (>) and Greater than or equal to (>=): Checks if one value is greater than another, or greater than or equal to.

x = 8
y = 6
print(x > y)   #Output: True
print(x >= y)  #Output: True

#Less than (<) and Less than or equal to (<=): Checks if one value is less than another, or less than or equal to.

x = 3
y = 5
print(x < y)  #Output: True
print(x <= y) #Output: True
