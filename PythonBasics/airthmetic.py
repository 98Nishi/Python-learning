# Arithmetic operations

# Addition
print(10 + 3)

# Multiplication
print(10 * 3)

# Subtraction
print(10 - 3)

# Division
print(10 / 3)

# Floor Division (integer division)
print(10 // 3)

# Exponentiation (10 raised to the power of 3)
print(10 ** 3)

# Modulo (remainder of 10 divided by 3)
print(10 % 3)
